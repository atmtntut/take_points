def draw_grid(data):
    c, r = data.shape[0], data.shape[1]
    row_labels = list(range(0, r, 1))
    column_labels = list(range(0, c, 1))
    fig, ax = plt.subplots()
    fig.set_figheight(c/10)
    fig.set_figwidth(r/10)
    heatmap = ax.pcolor(data, cmap=plt.cm.Blues)

    ax.set_xticks(np.arange(r)+0.5, minor=False)
    ax.set_yticks(np.arange(c)+0.5, minor=False)

    ax.invert_yaxis()
    ax.xaxis.tick_top()

    ax.set_xticklabels(row_labels, minor=False)
    ax.set_yticklabels(column_labels, minor=False)

    plt.show()

